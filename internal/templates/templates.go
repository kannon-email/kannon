// Package templates defines the Template domain entity per CONTEXT.md:
// the HTML/text body authored by a tenant and referenced from a Batch.
// The storage row is sqlc.Template; the on-the-wire payload is the proto
// Template; the domain entity is templates.Template.
//
// Template type values ("transient" / "template") are NOT renamed in this
// slice — the wire/DB-breaking rename to "persistent" is deferred per the
// PRD scope.
package templates

import (
	"crypto/rand"
	"errors"
	"fmt"
	"time"

	pb "github.com/kannon-email/kannon/proto/kannon/admin/apiv1"
	"github.com/lucsky/cuid"
)

// Domain errors.
var (
	ErrTemplateNotFound = errors.New("template not found")
)

// Type is the lifecycle classification of a Template.
//
// A persistent template is authored once and reused across Batches; a
// transient template is generated by the Mailer API to capture the
// rendered HTML for a single Batch (e.g. SendHTML or a per-call
// global-fields override).
type Type string

const (
	// TypeTransient marks a template generated for a single send.
	TypeTransient Type = "transient"
	// TypePersistent marks a template authored by the tenant for reuse.
	// The DB enum value remains "template" — see package doc.
	TypePersistent Type = "template"
)

// Template is the domain entity for a tenant-authored email body.
type Template struct {
	templateID string
	html       string
	title      string
	domain     string
	typ        Type
	createdAt  time.Time
	updatedAt  time.Time
}

// NewPersistent creates a new persistent Template with a freshly generated
// ID. createdAt/updatedAt are populated by the repository on Create.
func NewPersistent(domain, html, title string) (*Template, error) {
	if domain == "" {
		return nil, fmt.Errorf("domain is required")
	}
	id, err := newTemplateID(domain)
	if err != nil {
		return nil, err
	}
	return &Template{
		templateID: id,
		html:       html,
		title:      title,
		domain:     domain,
		typ:        TypePersistent,
	}, nil
}

// NewTransient creates a new transient Template with a freshly generated ID.
func NewTransient(domain, html string) (*Template, error) {
	if domain == "" {
		return nil, fmt.Errorf("domain is required")
	}
	id, err := newTemplateID(domain)
	if err != nil {
		return nil, err
	}
	return &Template{
		templateID: id,
		html:       html,
		domain:     domain,
		typ:        TypeTransient,
	}, nil
}

// LoadParams contains all fields needed to rehydrate a Template from storage.
type LoadParams struct {
	TemplateID string
	Html       string
	Title      string
	Domain     string
	Type       Type
	CreatedAt  time.Time
	UpdatedAt  time.Time
}

// Load rehydrates a Template from stored data (used by repository implementations).
func Load(p LoadParams) *Template {
	return &Template{
		templateID: p.TemplateID,
		html:       p.Html,
		title:      p.Title,
		domain:     p.Domain,
		typ:        p.Type,
		createdAt:  p.CreatedAt,
		updatedAt:  p.UpdatedAt,
	}
}

// Getters

func (t *Template) TemplateID() string   { return t.templateID }
func (t *Template) Html() string         { return t.html }
func (t *Template) Title() string        { return t.title }
func (t *Template) Domain() string       { return t.domain }
func (t *Template) Type() Type           { return t.typ }
func (t *Template) CreatedAt() time.Time { return t.createdAt }
func (t *Template) UpdatedAt() time.Time { return t.updatedAt }

// Mutators

// SetHTML overwrites the rendered body. Used by Repository.Update.
func (t *Template) SetHTML(html string) { t.html = html }

// SetTitle overwrites the title. Used by Repository.Update.
func (t *Template) SetTitle(title string) { t.title = title }

// Pb translates to the proto wire type.
func (t *Template) Pb() *pb.Template {
	return &pb.Template{
		TemplateId: t.templateID,
		Html:       t.html,
		Title:      t.title,
		Type:       string(t.typ),
	}
}

func newTemplateID(domain string) (string, error) {
	id, err := cuid.NewCrypto(rand.Reader)
	if err != nil {
		return "", err
	}
	return fmt.Sprintf("template_%v@%v", id, domain), nil
}
